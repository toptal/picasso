/**
 * bin/lib/failure-classifier.ts
 *
 * Phase 3.2 — classifies a failed CI check into one of four next-action
 * categories. Pure functions: take the check name + log excerpt + workflow
 * descriptor; return a structured recommendation. Caller (orchestrator-core
 * Phase 3.3) executes on the recommendation: auto-fix locally and push, feed
 * to agent for source edits, or escalate to human.
 *
 * Design constraints:
 *
 *   1. Heuristics, not perfect parsing. CI logs are unstructured; we err on
 *      the side of escalating when patterns don't clearly match. Confidence
 *      scoring isn't worth the complexity here — a binary decision with a
 *      conservative default is sufficient.
 *
 *   2. No I/O. The caller fetches logs via `gh api .../jobs/<id>/logs`; this
 *      module just inspects strings. Keeps the unit tests trivial (string
 *      fixtures in, classification out).
 *
 *   3. Migration-aware but not migration-specific. The patterns covered here
 *      (snapshot, lint, tsc, jest, cypress, happo, danger) generalize to any
 *      Picasso PR. A workflow can override specific rules via
 *      `Workflow.failureClassifier`.
 *
 * Classification categories:
 *
 *   - auto-fix-snapshot: rerun jest with `-u` on the failing test paths,
 *     commit the regenerated snapshots, push, re-poll CI. Idempotent for
 *     non-snapshot failures (jest -u is a no-op for assertion failures).
 *
 *   - auto-fix-lint: rerun `davinci-syntax lint code` (no --check) on the
 *     failing files; commit the auto-fix output; push.
 *
 *   - feed-to-agent: fold the failure into the agent's iter N+1 prompt; the
 *     agent makes source edits, gate runs locally, push delta. This is the
 *     most expensive recovery path so it's reserved for failures that need
 *     genuine code changes (TSC, Jest assertion regressions, Cypress
 *     functional failures, build errors).
 *
 *   - escalate: orchestrator transitions item to needs_human. Used for
 *     failures that need judgment outside the agent's surface (Happo visual
 *     diffs needing designer review, Danger meta-rules that suggest the
 *     orchestrator's commit/PR-title format itself is broken, anything
 *     unrecognized).
 */

export type FailureClass =
  | 'auto-fix-snapshot'
  | 'auto-fix-lint'
  | 'feed-to-agent'
  | 'escalate'

// Note: an `'auto-fix-rerun'` class existed previously (Tier 2 batch B
// Slice 4 — Happo-flake mitigation via empty-commit retrigger). Removed
// in v4 Step 4 (strict Happo gate at gate time). If transient flake
// patterns surface in NEW failure modes (network blips on Cypress, etc.),
// re-introduce here behind a workflow-specific opt-in rather than a
// blanket reclassification.

export interface FailureClassification {
  /** Action recommendation for orchestrator-core to execute. */
  class: FailureClass
  /** Short human-readable reason (logs + telemetry). */
  reason: string
  /** Stage label (for prompt assembly when class === feed-to-agent). */
  stage?: string
  /** Truncated log excerpt for prompt assembly. */
  excerpt?: string
  /**
   * For auto-fix-snapshot / auto-fix-lint: the file/test paths to re-run
   * the auto-fix command on. Extracted from the log via best-effort parsing.
   * Empty array means "couldn't extract paths" — caller should fall back to
   * a broader scope (e.g. full-repo `lint --fix`) or escalate.
   */
  paths: readonly string[]
}

export interface CheckLike {
  /** Check name from gh API (e.g. "Static checks", "Happo (Picasso/Cypress)"). */
  name: string
  /** Conclusion (FAILURE / CANCELLED / etc.). */
  conclusion: string
}

const SNAPSHOT_MARKER = /Snapshot name: `[^`]+`/
const LINT_FIXABLE_MARKER = /potentially fixable with the .*--fix.* option/
const TSC_ERROR_MARKER = /error TS\d{3,5}:/
const ASSERTION_MARKERS = [
  /expect\(.*\)\.toBe\(/,
  /expect\(.*\)\.toEqual\(/,
  /expect\(.*\)\.toHaveTextContent\(/,
  /expect\(.*\)\.toHaveBeenCalled/,
]
const CYPRESS_FAIL_MARKER =
  /CypressError|Cypress could not verify|cy\..* timed out/
const HAPPO_MARKERS = [
  /happo\.io\/[^\s]*\/compare\//,
  /Happo\s.*\sdiffs?\sfound/i,
  /\bHappo CLI\b/,
]
const DANGER_MARKERS = [
  /dangerJS/i,
  /Generated by .*?dangerJS/,
  /\bDanger:\s+[✖✗✕✘x×]/, // e.g. "Danger: ✖"
]
const BUILD_MARKERS = [
  /Cannot find module/,
  /Module not found/,
  /Failed to compile/,
]

const MAX_EXCERPT_BYTES = 6000

/**
 * Parse "FAIL packages/.../test.tsx" lines into deduplicated test paths.
 * Used for both snapshot and assertion failure scoping.
 *
 * Match strategy: the FAIL marker can appear with various leading content
 * depending on log source.
 *   - Local jest stdout: line starts with `FAIL packages/...`
 *   - GitHub Actions log via `gh api .../jobs/<id>/logs`: line starts with
 *     an ISO timestamp like `2026-05-06T11:47:20.1234567Z FAIL packages/...`
 *   - GitHub Actions log via `gh run view --log-failed`: prefixes with
 *     `<job-name>\t<step-name>\t<timestamp> ...`
 *
 * Anchoring to `^FAIL` (canary 28 PR #4934) silently dropped paths from the
 * timestamped CI log → empty paths → no auto-fix-snapshot action → escalate.
 * Use a permissive look-behind that accepts whitespace or end-of-line before
 * FAIL to handle all three formats.
 */
function extractFailedTestPaths(log: string): readonly string[] {
  const paths = new Set<string>()
  // Primary marker: jest's `FAIL packages/.../<name>.test.tsx` line.
  const failMarkers =
    log.match(/(?:^|\s)FAIL\s+(\S+\.(?:test|spec)\.[a-z]+)/g) ?? []

  for (const line of failMarkers) {
    const stripped = line.replace(/^.*FAIL\s+/, '').trim()

    if (stripped) {
      paths.add(stripped)
    }
  }
  // Fallback marker (added 2026-05-18 after Modal PR #4967): CI's "Static
  // checks" job runs jest with a formatter that doesn't print the FAIL
  // header — instead the failing test file appears in the call-stack
  // line like `at Object.toMatchSnapshot (packages/.../test.tsx:29:25)`.
  // Without this fallback the auto-fix-snapshot path silently ran with
  // paths=[] and skipped the jest -u step entirely.
  const stackMatches =
    log.match(
      /\bat\s+[\w$.<>]+\s+\((packages\/[^):\s]+\.(?:test|spec)\.[a-z]+):\d+/g
    ) ?? []

  for (const line of stackMatches) {
    const match = line.match(/\((packages\/[^):\s]+\.(?:test|spec)\.[a-z]+):/)

    if (match && match[1]) {
      paths.add(match[1])
    }
  }

  return [...paths]
}

/**
 * Parse eslint output for the file paths that have at least one error.
 * Recognises the default eslint formatter:
 *
 *     /abs/path/to/file.ts
 *       12:34  error  ...
 *
 * Returns repo-relative paths if possible (strips a known prefix), else
 * absolute paths.
 */
function extractLintFiles(log: string, repoRoot?: string): readonly string[] {
  // Strip ANSI escape sequences first. CI's color-on lint output wraps file
  // paths and error markers in `[Xm` codes, breaking the line-shape
  // regexes below. Local lint (`pnpm davinci-syntax lint code --check ...`)
  // emits plain text without ANSI, so this only matters for CI logs — but
  // we strip universally for safety.
  // eslint-disable-next-line no-control-regex
  const stripped = log.replace(/\u001b\[[0-9;]*m/g, '')
  const lines = stripped.split('\n')
  const files: string[] = []
  let currentFile: string | null = null

  for (const line of lines) {
    // File header: a non-indented line ending in a recognized JS/TS extension.
    const fileMatch = line.match(/^(\S+\.(?:tsx?|jsx?|mjs|cjs))\s*$/)

    if (fileMatch) {
      currentFile = fileMatch[1]
      continue
    }

    // Error row: indented, contains "error" (not "warning").
    if (currentFile && /^\s+\d+:\d+\s+error\b/.test(line)) {
      const rel =
        repoRoot && currentFile.startsWith(repoRoot)
          ? currentFile.slice(repoRoot.length).replace(/^\/+/, '')
          : currentFile

      if (!files.includes(rel)) {
        files.push(rel)
      }
      // Don't reset currentFile — multiple errors per file are common.
    }
  }

  return files
}

/** Take the trailing N bytes; useful for prompt context that needs the tail. */
function tail(s: string, bytes: number): string {
  return s.length <= bytes ? s : '...\n' + s.slice(-bytes)
}

/**
 * Extract the package.json paths from syncpack's `list-mismatches` output.
 * syncpack output shape (one line per mismatch):
 *   ✘ <name> <wrote> → <expected> <packageJsonPath> > <depsField> [<reason>]
 */
function extractPackageJsonsFromSyncpack(log: string): readonly string[] {
  const out = new Set<string>()
  const re = /(packages\/[^\s>]+\/package\.json)/g
  let m: RegExpExecArray | null

  while ((m = re.exec(log)) !== null) {
    if (m[1]) {
      out.add(m[1])
    }
  }

  return [...out]
}

/**
 * Classify a failed check. Order matters: more specific patterns are checked
 * before more general ones (e.g. Happo before generic Cypress). Also: name-
 * based hints win over log-based hints (e.g. a check named "Happo (Picasso/
 * Cypress)" → escalate even if its log doesn't contain a Happo URL).
 */
export function classifyCIFailure(
  check: CheckLike,
  log: string,
  opts: { repoRoot?: string } = {}
): FailureClassification {
  const name = check.name.toLowerCase()

  // 1. Happo — feed-to-agent (Part 4, 2026-05-13). Earlier policy
  //    escalated Happo failures unconditionally to designer review. New
  //    policy: agent attempts to iterate on Happo diffs same as lint/test
  //    failures. The agent reads the Happo report URL from the log,
  //    inspects each diff via the report, and decides per slot:
  //      - regression → edit source to match baseline; re-run gate
  //      - intentional → mark in changeset's `## Intentional visual
  //        changes` section; gate ignores listed slots in subsequent iters
  //    After the iteration budget (--max-iterations / --max-ci-iterations)
  //    is exhausted with same diff-set persisting, the outer loop's
  //    stuck-detection triggers escalation to designer review naturally.
  //    No separate MAX_HAPPO_ITERATIONS counter — Happo iteration shares
  //    the existing iteration budget (operator's choice 2026-05-13).
  if (name.includes('happo') || HAPPO_MARKERS.some(re => re.test(log))) {
    return {
      class: 'feed-to-agent',
      reason:
        'Happo diffs — agent inspects report URL + decides per slot ' +
        '(regression: fix source; intentional: mark in changeset). ' +
        'After iteration budget exhausted with same diffs, stuck-detection ' +
        'escalates to designer.',
      stage: 'happo',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: [],
    }
  }

  // 2. Danger — meta-rule failure means the orchestrator's commit/PR-title
  // format itself is wrong. Agent can't fix this; needs operator.
  if (name === 'danger' || DANGER_MARKERS.some(re => re.test(log))) {
    return {
      class: 'escalate',
      reason: 'Danger CI failed — orchestrator commit/title format bug',
      stage: 'danger',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: [],
    }
  }

  // 3. Snapshot regressions — auto-fixable via jest -u. Detection: log has
  // at least one "Snapshot name: `...`" line AND no non-snapshot assertion
  // failures. The latter check matters because a single failed test file
  // might have BOTH a snapshot mismatch and a real assertion failure; jest
  // -u would mask the assertion bug. We err on the safe side: if any
  // assertion-style expect is present, classify as feed-to-agent instead.
  const hasSnapshot = SNAPSHOT_MARKER.test(log)
  const hasNonSnapshotAssertion = ASSERTION_MARKERS.some(re => re.test(log))

  if (hasSnapshot && !hasNonSnapshotAssertion) {
    const testPaths = extractFailedTestPaths(log)

    return {
      class: 'auto-fix-snapshot',
      reason: `Snapshot regression in ${
        testPaths.length || 'unknown'
      } test file(s)`,
      stage: 'jest',
      paths: testPaths,
    }
  }

  // 4. Auto-fixable lint. ESLint's "potentially fixable" marker is the
  // canonical signal that --fix would resolve all errors.
  if (LINT_FIXABLE_MARKER.test(log)) {
    const lintFiles = extractLintFiles(log, opts.repoRoot)

    return {
      class: 'auto-fix-lint',
      reason: `${lintFiles.length} file(s) have eslint errors fixable via --fix`,
      stage: 'lint',
      paths: lintFiles,
    }
  }

  // 5. TSC errors — feed to agent (type errors usually need code edits, not
  // mechanical fixes).
  if (TSC_ERROR_MARKER.test(log)) {
    return {
      class: 'feed-to-agent',
      reason: 'TypeScript compile errors',
      stage: 'tsc',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: [],
    }
  }

  // 6. Jest assertion failures (non-snapshot). The agent gets a real
  // expected/received delta to reason about.
  if (hasNonSnapshotAssertion) {
    return {
      class: 'feed-to-agent',
      reason: 'Jest assertion failure(s)',
      stage: 'jest',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: extractFailedTestPaths(log),
    }
  }

  // 7. Cypress functional failures (timeouts, missing elements, network).
  if (CYPRESS_FAIL_MARKER.test(log)) {
    return {
      class: 'feed-to-agent',
      reason: 'Cypress functional test failure',
      stage: 'cypress',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: [],
    }
  }

  // 8. Build / module-resolution failures.
  if (BUILD_MARKERS.some(re => re.test(log))) {
    return {
      class: 'feed-to-agent',
      reason: 'Build / module-resolution failure',
      stage: 'build',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: [],
    }
  }

  // 9. Lint without "fixable" marker — feed to agent (genuine code edits
  // needed, e.g. unused vars, broken imports).
  if (/error\s+(?:no-unused-vars|no-undef|@typescript-eslint\/)/.test(log)) {
    return {
      class: 'feed-to-agent',
      reason: 'Lint errors that need code edits (not auto-fixable)',
      stage: 'lint',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: extractLintFiles(log, opts.repoRoot),
    }
  }

  // 10. Syncpack mismatches — feed to agent with explicit guidance.
  // CI's "Static checks" job runs `pnpm syncpack list-mismatches`; if any
  // mismatches exist, the job fails. The agent can fix these by editing
  // package.json with the correct version (caret-prefix for npm deps,
  // exact version for workspace deps). See PROMPT-light/heavy §2/§4.
  if (
    /HighestSemverMismatch|LocalPackageMismatch|syncpack[: ]list-mismatches/.test(
      log
    )
  ) {
    return {
      class: 'feed-to-agent',
      reason:
        'Syncpack dependency-version mismatch (npm: use ^x.y.z; workspace: use exact version)',
      stage: 'syncpack',
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: extractPackageJsonsFromSyncpack(log),
    }
  }

  // 11. "Static checks" / generic CI catch-all — try to feed to agent if
  // the log has anything actionable. Many "Static checks" failures are
  // syncpack, lint, or pnpm-install issues that look generic at the job-
  // name level but classify cleanly once we read the log. If we got here
  // without matching a specific pattern, give the agent the log tail and
  // let it figure it out — better than escalating without trying.
  if (/static checks|build packages/.test(name)) {
    return {
      class: 'feed-to-agent',
      reason: `Generic "${check.name}" failure — feeding log tail to agent`,
      stage: check.name,
      excerpt: tail(log, MAX_EXCERPT_BYTES),
      paths: [],
    }
  }

  // 12. Default: escalate. Better to surface to a human than to flail.
  return {
    class: 'escalate',
    reason: `unclassified CI failure on "${check.name}" (${check.conclusion})`,
    stage: check.name,
    excerpt: tail(log, MAX_EXCERPT_BYTES),
    paths: [],
  }
}

// ----- Legacy-defense pattern detection (§F of Phase 2 doctrine plan) -----
//
// When the agent ships rung-5 inline `style` overrides on Base UI parts
// (`style={{ translate / position / transform / left / top: … }}`) AND retains
// legacy margin/offset literals nearby (`-mt-[Npx]` / `-ml-[Npx]`), it's
// usually defending a legacy approximation of what Base UI now does
// geometrically exactly. The doctrine-clean answer is rung -1: remove the
// legacy literals AND the override together, accept Base UI's geometry, propose
// the sub-pixel diff as "intentional improvement" per
// references/practices.md §"Visual parity by default …".
//
// Slider v2 PR #4975 shipped both `style={{ translate: 'none' }}` AND
// `-mt-[7px] -ml-[6px]` together. PR #4976 (vedrani fork) eliminated BOTH
// in commit 4f5951f and accepted a sub-pixel Happo delta classified as
// touch-target accessibility improvement.
//
// This detector is meant to be called by orchestrator-core AFTER the agent
// commits an iter (N ≥ 2 with prior structural Happo diffs) and BEFORE the
// next iter is dispatched. If it returns non-null, the orchestrator prepends
// the hint to the next agent prompt so the agent reconsiders rung -1.

export interface LegacyDefenseHint {
  /** Doctrine reference for the corrective guidance. */
  doctrineRef: string
  /** Human-readable corrective hint, ready to embed in an agent prompt. */
  hint: string
  /** Files in which the pattern was detected (deduplicated). */
  files: readonly string[]
}

const RUNG_5_OVERRIDE_RE =
  /style=\{\{\s*(?:translate|position|transform|top|left|right|bottom)\s*:/
const LEGACY_MARGIN_RE = /-m[tlrb]-\[\d+px\]/

/**
 * Detect the legacy-defense pattern in a unified git diff.
 *
 * Heuristic: within any single `+++ b/<file>` hunk, the diff added (a) at
 * least one rung-5 `style={{ translate|position|transform|top|left|right|
 * bottom: … }}` override AND (b) at least one `-mt-[Npx]` / `-ml-[Npx]` /
 * `-mr-[Npx]` / `-mb-[Npx]` legacy margin literal. The two together signal
 * "agent is using rung 5 to defend a legacy approximation" — the doctrine-
 * clean answer is rung -1 (remove both).
 *
 * Returns null when the pattern is not detected. False positives are
 * possible (e.g., the legacy margin is genuinely needed for an unrelated
 * visual concern) — the hint is advisory, not blocking. The agent should
 * use its `style`-vs-`-mt`-relationship judgment per the PROMPT-light
 * §"Reasoning checklist" question 2 ("validate geometrically").
 *
 * @param diff Unified `git diff` output (e.g., from `git diff HEAD~1 HEAD`)
 *             for the migrating package.
 */
export function detectLegacyDefensePattern(
  diff: string
): LegacyDefenseHint | null {
  // Split diff into per-file hunks. Header form: `diff --git a/X b/Y`.
  // We only care about added lines (`+ …`) within each file's hunk.
  const fileMatches = new Map<string, string[]>()
  let currentFile: string | null = null
  let currentLines: string[] = []

  for (const line of diff.split('\n')) {
    const fileHeader = line.match(/^\+\+\+ b\/(.+)$/)

    if (fileHeader) {
      if (currentFile !== null) {
        fileMatches.set(currentFile, currentLines)
      }
      currentFile = fileHeader[1]
      currentLines = []
      continue
    }

    // Skip diff metadata; only collect added (`+ …`) content lines.
    if (line.startsWith('+') && !line.startsWith('+++')) {
      currentLines.push(line.slice(1))
    }
  }
  if (currentFile !== null) {
    fileMatches.set(currentFile, currentLines)
  }

  const flaggedFiles: string[] = []

  for (const [file, addedLines] of fileMatches.entries()) {
    const hasRung5Override = addedLines.some(l => RUNG_5_OVERRIDE_RE.test(l))
    const hasLegacyMargin = addedLines.some(l => LEGACY_MARGIN_RE.test(l))

    if (hasRung5Override && hasLegacyMargin) {
      flaggedFiles.push(file)
    }
  }

  if (flaggedFiles.length === 0) {
    return null
  }

  const filesList = flaggedFiles.map(f => `  - ${f}`).join('\n')
  const hint =
    `Detected rung-5 inline \`style\` override + adjacent legacy margin literals in:\n` +
    `${filesList}\n\n` +
    `This pattern usually means you're defending a legacy approximation of what Base UI now does geometrically exactly (e.g., \`-mt-[7px] -ml-[6px]\` was approximating half-of-15px-thumb; Base UI's \`translate: -50% -50%\` does this exactly). The doctrine-clean answer is rung -1:\n\n` +
    `  1. Remove BOTH the legacy margins AND the inline \`style\` override.\n` +
    `  2. Accept Base UI's geometry as-is.\n` +
    `  3. Post a MEDIUM PR comment naming the sub-pixel Happo diff as "intentional improvement" with the geometric reasoning.\n\n` +
    `Slider PR #4976 (vedrani) commit 4f5951f demonstrated this — both \`style={{ translate: 'none' }}\` and \`-mt-[7px] -ml-[6px]\` removed together; Happo classified the resulting sub-pixel diff as approved-delta (touch-target accessibility).\n\n` +
    `See references/base-ui-styling.md §7.1 rung -1 and references/practices.md §"Visual parity by default; geometric improvements via approved-delta channel".`

  return {
    doctrineRef: 'references/base-ui-styling.md §7.1 rung -1',
    hint,
    files: flaggedFiles,
  }
}
